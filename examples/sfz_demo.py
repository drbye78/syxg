def generate_sine_wave_sample(frequency=440.0, duration=1.0, sample_rate=44100):
    """
    Generate a simple sine wave sample for demo purposes.
    In a real implementation, this would be replaced with actual audio files.
    """
    t = np.linspace(0, duration, int(sample_rate * duration), False)
    # Create a more complex waveform (mix of harmonics for piano-like sound)
    wave = (
        np.sin(2 * np.pi * frequency * t) * 0.6
        + np.sin(2 * np.pi * frequency * 2 * t) * 0.3
        + np.sin(2 * np.pi * frequency * 3 * t) * 0.1
    )

    # Apply simple envelope (attack/decay)
    attack_samples = int(0.01 * sample_rate)  # 10ms attack
    decay_samples = int(0.3 * sample_rate)  # 300ms decay

    envelope = np.ones_like(wave)
    envelope[:attack_samples] = np.linspace(0, 1, attack_samples)
    envelope[attack_samples : attack_samples + decay_samples] = np.linspace(1, 0.7, decay_samples)

    wave *= envelope

    # Convert to 16-bit PCM
    wave = np.clip(wave * 32767, -32767, 32767).astype(np.int16)

    return wave.tobytes()


def demo_sfz_engine():
    """Demonstrate the SFZ engine capabilities."""
    print("🎹 SFZ SYNTHESIS ENGINE DEMONSTRATION")
    print("=" * 60)

    # Create demo SFZ instrument
    sfz_file = create_demo_sfz_instrument()

    # Initialize SFZ engine
    print("\n🎛️  Initializing SFZ Engine...")
    sfz_engine = SFZEngine()

    # Load the demo instrument
    print("📂 Loading SFZ instrument...")
    success = sfz_engine.load_instrument(sfz_file)
    if not success:
        print("❌ Failed to load SFZ instrument")
        return

    print("✅ SFZ instrument loaded successfully!")

    # Display instrument info
    instruments = sfz_engine.get_loaded_instruments()
    print(f"📋 Loaded instruments: {instruments}")

    engine_info = sfz_engine.get_engine_info()
    print(f"🎛️  Engine: {engine_info['name']}")
    print(f"📊 Capabilities: {len(engine_info['capabilities'])} features")
    print(f"🎵 Supported formats: {engine_info['supported_sample_formats']}")

    # Test region selection
    print("\n🎯 Testing Region Selection...")

    # Test different velocities for the same note
    test_note = 60  # Middle C
    velocities = [32, 64, 96]  # Low, medium, high velocity

    for vel in velocities:
        regions = sfz_engine.get_regions_for_note(test_note, vel)
        print(f"   Note {test_note} (C4), Velocity {vel}: {len(regions)} regions")

    # Generate audio samples
    print("\n🎵 Generating Audio Samples...")

    # Test different velocities
    for vel in velocities:
        print(f"   Generating audio for velocity {vel}...")
        audio = sfz_engine.generate_samples(test_note, vel, {}, block_size=4410)  # 100ms
        print(f"   ✓ Generated {len(audio)} samples ({len(audio) / 44.1:.1f}ms)")

    print("✅ SFZ Engine demonstration completed!")


